    public void loadActivos(){
        DefaultTableModel model = (DefaultTableModel) tablaActiosJT.getModel();
        int rowCount = model.getRowCount();
        //Remove rows one by one from the end of the table
        for (int i = rowCount - 1; i >= 0; i--) {
            model.removeRow(i);
        }
        for(Activo a : empresa.getActivos()){
            Object[] row = {a.getNombre(), a.getTipos()};
            model.addRow(row);
        } 

        
    }

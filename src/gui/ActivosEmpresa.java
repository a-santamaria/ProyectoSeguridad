    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int index = tablaActiosJT.getSelectedRow();
        if(index == -1){
            JOptionPane.showMessageDialog(this, "Ninguna Activo Seleccionada", "Error", JOptionPane.ERROR_MESSAGE);
        }else{
            String message = ("Borrar empresa el activo?") ;
            int reply = JOptionPane.showConfirmDialog(null, message  , "Borrar", JOptionPane.YES_OPTION);
            if(reply == JOptionPane.YES_OPTION){
                empresa.getActivos().remove(index);
                loadActivos();
                Archivos.guardarArchivo();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed


